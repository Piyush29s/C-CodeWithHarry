#include <stdio.h>
int main()
{
    int a = 4;
    // int can only have integer value
    float b = 8.5;
    char c = 'u';
    int d = 45;

    printf("The value of a is %d", a);
    printf("\nThe value of b is %f \n", b);
    printf("The value of c is %c \n", c);
    printf("Sum of a and d is %d", a + d);
    return 0;
}